"""Emit per-surface design token files from ``cortex/libs/design/tokens.yaml``.

Run via::

    python -m cortex.scripts.sync_design_tokens

Outputs (idempotent — re-running with no YAML change produces a no-op diff):

* ``cortex/apps/desktop_shell/tokens.py``         — Python constants + helpers
* ``cortex/apps/browser_extension/design-tokens.ts`` — TypeScript ``CX`` object
* ``cortex/apps/vscode_extension/src/design-tokens.ts`` — TS subset for the panel

The emitter is intentionally simple (no Jinja, no codegen framework) so the
output is trivially reviewable in PR diffs.
"""

from __future__ import annotations

import sys
from pathlib import Path
from typing import Any

import yaml

_ROOT = Path(__file__).resolve().parent.parent.parent
_TOKENS_YAML = _ROOT / "cortex" / "libs" / "design" / "tokens.yaml"
_PY_OUT = _ROOT / "cortex" / "apps" / "desktop_shell" / "tokens.py"
_BROWSER_OUT = _ROOT / "cortex" / "apps" / "browser_extension" / "design-tokens.ts"
_VSCODE_OUT = _ROOT / "cortex" / "apps" / "vscode_extension" / "src" / "design-tokens.ts"


def _load() -> dict[str, Any]:
    with _TOKENS_YAML.open() as f:
        data = yaml.safe_load(f)
    if not isinstance(data, dict):
        raise ValueError("tokens.yaml must be a mapping at top level")
    return data


