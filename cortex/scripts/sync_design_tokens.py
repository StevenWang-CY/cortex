# ---------------------------------------------------------------------------
# TypeScript emitter (VS Code panel — uses theme variables; tokens are minimal)
# ---------------------------------------------------------------------------

def emit_vscode_ts(data: dict[str, Any]) -> str:
    brand = data["brand"]
    typo = data["typography"]
    state = data["state"]
    light = data["semantic"]["light"]

    out = [_TS_HEADER]
    out.append("// VS Code panel adopts the user's editor theme via CSS variables;\n")
    out.append("// the brand accent (terracotta) is the only fixed identity element.\n\n")
    out.append("export const PANEL_BRAND_ACCENT = ")
    out.append(f'"{brand["accent"]["hex"]}";\n')
    out.append("export const PANEL_BRAND_ACCENT_DARK = ")
    out.append(f'"{brand["accent_dark"]["hex"]}";\n\n')

    out.append("export const PANEL_FS = {\n")
    for k, v in typo["scale"].items():
        out.append(f'    {k}: {v},\n')
    out.append("} as const;\n\n")

    out.append("export const PANEL_STATE_LABELS: Record<string, string> = {\n")
    for k, v in state.items():
        out.append(f'    {k}: "{v["label"]}",\n')
    out.append("};\n\n")

    out.append("// Optional state tints when the editor theme can't express the role.\n")
    out.append("export const PANEL_STATE_HEX_LIGHT: Record<string, string> = {\n")
    for k, v in state.items():
        role = v["accent_role"]
        if role == "brand":
            hex_value = brand["accent"]["hex"]
        elif role in light:
            hex_value = light[role]["hex"]
        else:
            hex_value = "#999999"
        out.append(f'    {k}: "{hex_value}",\n')
    out.append("};\n")
    return "".join(out)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main(argv: list[str] | None = None) -> int:
    data = _load()
    py_out = emit_python(data)
    browser_out = emit_browser_ts(data)
    vscode_out = emit_vscode_ts(data)

    _PY_OUT.write_text(py_out)
    _BROWSER_OUT.write_text(browser_out)
    _VSCODE_OUT.parent.mkdir(parents=True, exist_ok=True)
    _VSCODE_OUT.write_text(vscode_out)

    print(f"Wrote {_PY_OUT.relative_to(_ROOT)}", file=sys.stderr)
    print(f"Wrote {_BROWSER_OUT.relative_to(_ROOT)}", file=sys.stderr)
    print(f"Wrote {_VSCODE_OUT.relative_to(_ROOT)}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
